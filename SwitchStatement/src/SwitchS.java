public class SwitchS {
    public static void main(String[] args) {

        int n=10;

        switch (n) {
            case 10:
                System.out.println("=10");
            case 90:
                System.out.println("=90");
            case -3:
                System.out.println("-3");
            default:
                System.out.println("none of the above");

        }
    }
}
