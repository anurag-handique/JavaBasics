public class Breakstatement {
    public static void main(String[] args) {

        int n=10;

        switch (n) {

            case 40:
            System.out.println("=40");
                break;
            case 20:
                System.out.println("=20");
                break;
            default:
                System.out.println("none of the above");
                break;
            case 30:
                System.out.println("=30");
                break;
        }

    }
}
